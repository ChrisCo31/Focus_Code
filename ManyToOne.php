    <h2> Many To One ou n..1 </h2>
      </br>
      <p> Elle correspond à une relation ou une entite A peut avoir plusieurs relations avec une entite B </p>
      <p> Elle correspond à la relation entre notre entite Media et notre entite Article </p>
      <p> L'entite propiretaire sera l'entite Article, et l'entite media sera l'entite inverse. De maniere systematique c'est le cote many d'une relation qui est proprietaire de la relation.</p>
      <p> Ajout des getters et setters correspondant. La relation focntionne de maniere assez similaire avec la relation OneToOne</p>

